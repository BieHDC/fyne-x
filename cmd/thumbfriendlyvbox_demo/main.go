func main() {
	a := app.New()
	w := a.NewWindow("Common Phone Sized Window")
	w.Resize(fyne.NewSize(300, 450))
	c := container.NewAppTabs(
		container.NewTabItem("Friendly", friendly()),
		container.NewTabItem("Unfriendly", unfriendly()),
		container.NewTabItem("Friendly Scroll", friendlywithscroll()),
	)
	c.SetTabLocation(container.TabLocationBottom) // also part of thumb friendlyness

	w.SetContent(container.NewStack(
		c,
		// weirdest way to fake a thumb for sure
		container.NewGridWithColumns(2, widget.NewLabel(""), widget.NewLabel(""), widget.NewLabel(""), canvas.NewImageFromReader(bytes.NewReader(thumbpng[:]), "worstfakethumbyoueverseen")),
	))

	w.ShowAndRun()
}

func friendly() fyne.CanvasObject {
	return layout.NewBottomUp(examplecontent("Thumb Friendly", 10)...)
}

func unfriendly() fyne.CanvasObject {
	return container.NewVBox(examplecontent("Thumb Unfriendly", 10)...)
}

func friendlywithscroll() fyne.CanvasObject {
	return container.NewVScroll(layout.NewBottomUp(examplecontent("Friendly Scroll", 50)...))
}

func examplecontent(title string, num uint) []fyne.CanvasObject {
	content := []fyne.CanvasObject{widget.NewLabel(title)}
	for i := uint(0); i < num; i += 2 {
		i := i
		content = append(content,
			// HBox, but stretched to width, generally nice layout idea by op!
			container.New(layout.NewHPortion([]float64{50, 50}),
				widget.NewButton(fmt.Sprintf("Item Number %d", i),
					func() {
						fmt.Printf("%s:%d\n", title, i)
					},
				),
				widget.NewButton(fmt.Sprintf("Item Number %d", i+1),
					func() {
						fmt.Printf("%s:%d\n", title, i+1)
					},
				),
			),
		)
	}

	return content
}

